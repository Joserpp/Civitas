    void setCasilla(Casilla casilla){
    
        this.casilla = casilla;
        
        String nombre = this.casilla.getNombre();
        
        NOMBRE.setText(nombre);
        
        if(!"Salida".equals(nombre)){
            if(!"Sorpresa".equals(nombre)){
                if(!"Parking PÃºblico".equals(nombre)){
                    
                    PanelCasillaCalle.setVisible(true);
                    
                    if(((CasillaCalle)this.casilla).tienePropietario()){
                        
                        n_propietario.setText(((CasillaCalle)casilla).getPropietario().getNombre());
                        
                    }
                    else
                        n_propietario.setText("No tiene");
                    
                    p_compra.setText(Float.toString(((CasillaCalle)this.casilla).getPrecioCompra()));
                    p_edificar.setText(Float.toString(((CasillaCalle)this.casilla).getPrecioEdificar()));
                    p_alquiler.setText(Float.toString(((CasillaCalle)this.casilla).getPrecioAlquilerCompleto()));
                }
            }
        }
        else{
        
            /*PROPIETARIO.setVisible(false);
            P_ALQUILER.setVisible(false);
            P_COMPRA.setVisible(false);
            P_EDIFICAR.setVisible(false);*/
            PanelCasillaCalle.setVisible(false);
        }
            
    }

